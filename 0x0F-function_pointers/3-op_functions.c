#include "3-calc.h"

/**
 * op_add - performs the addition operation.
 * @a: Integer.
 * @b: Integer.
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - performs the subtraction operation.
 * @a: First Integer.
 * @b: Second Integer.
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - performs the multiplication operation.
 * @a: Integer.
 * @b: Integer.
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - performs the division operation.
 * @a: First Integer.
 * @b: Second Integer.
 * Return: The division of a and b.
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - performs the modulus operation.
 * @a: First Integer.
 * @b: Second Integer.
 * Return: The modulus of a and b.
 */
int op_mod(int a, int b)
{
return (a % b);
}
